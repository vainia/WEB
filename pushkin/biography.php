				<p>
				  &nbsp;&nbsp;&nbsp;Со стороны отца происходил из обедневшего
              древнего боярского рода, представители которого славились в российской
              истории своим независимым и мятежным нравом: так, в 1697 Петр Великий
              казнил Федора Пушкина за причастность к стрелецкому бунту. Поэту
              выпала иная судьба: он немало претерпел и от Александра I, которого
              презирал, и от Николая I, который пытался его закрепостить духовно.
              <br>
            </p>
            <p>
				  &nbsp;&nbsp;&nbsp;С материнской стороны Пушкин был правнуком любимца Петра Великого
              Абрама Ганнибала, сына абиссинского царевича, подвластного турецкому
              султану. Восьмилетний Абрам был привезен в качестве заложника в
              Константинополь, где российский посланник купил его для своего царя.
              Его крестили в Вильно, в тогдашней Польше, и восприемниками были
              сам Петр и польская королева, жена Августа II. Петр привязался к
              мальчику; позднее он отправил его во Францию изучать военное дело.
              На участь Ганнибала прискорбно повлияла смерть Петра, но с воцарением
              его дочери Елизаветы он вернулся в милость, стяжал немалое богатство
              и достиг высокого поста. Ганнибалы продолжали служить в российской
              армии и на флоте. Мать поэта была внучкой Абрама Ганнибала.
			   </p>
            <p>
				  &nbsp;&nbsp;&nbsp;Летние месяцы 1805— 1810 гг. он обычно проводил
              у своей бабушки, Марии Алексеевны (урожденной Ганнибал), в подмосковном
              селе Захарове, близ Звенигорода. Эти ранние детские впечатления
              отразились в первых опытах пушкинских поэм, написанных несколько
              позже ("Монах", 1813; "Бова", 1814), в лицейских
              стихотворениях "Послание к Юдину" (1815), "Сон"
              (1816).
				  <br>
              &nbsp;&nbsp;&nbsp;Шесть лет Пушкин провел в Царскосельском Лицее,
              открытом 19 октября 1811 г. Здесь юный поэт пережил события Отечественной
              войны 1812 г.
				  <br>
              &nbsp;&nbsp;&nbsp;В начале 1815 г. Пушкин читает в присутствии Г.
              Р. Державина свое патриотическое стихотворение "Воспоминание
              в Царском Селе" (напечатано в журнале "Русский Музеум"
              в том же году за полной подписью автора).
				  <br>
              &nbsp;&nbsp;&nbsp;Еще на лицейской скамье Пушкин был принят в литературное
              общество "Арзамас", выступавшее против рутины и архаики
              в литературном деле.
				  <br>
              &nbsp;&nbsp;&nbsp;Ранняя поэзия поэта передавала ощущение быстротечности
              жизни, которая диктовала жажду наслаждений.
				  <br>
              В 1816 г. характер лирики Пушкина претерпевает существенные изменения.
              Элегия становится основным его жанром.
			   </p>
            <p>
				  &nbsp;&nbsp;&nbsp;Из Лицея Пушкин был выпущен в июне 1817 г. в
              чине коллежского секретаря (12-го класса) и определен в Коллегию
              иностранных дел. Он становится постоянным посетителем театра, принимает
              участие в заседаниях "Арзамаса", в 1819 г. вступает в
              члены литературно-театрального сообщества "Зеленая лампа",
              которым руководит "Союз благоденствия". Не принимая участия
              в деятельности первых тайных организаций, Пушкин тем не менее связан
              дружескими узами со многими активными членами декабристских обществ,
              пишет политические эпиграммы и стихи "К Чаадаеву" ("Любви,
              надежды, тихой славы...", 1818), "Вольность" (1818),
              "Н. Я. Плюсковой" (1818), "Деревня" (1819).
              В эти годы он занят работой над поэмой "Руслан и Людмила",
              начатой в Лицее и отвечавшей программным установкам литературного
              общества "Арзамас" о необходимости создания национальной
              богатырской поэмы. Поэма закончена в мае 1820 г. и после публикации
              вызвала ожесточенные отклики критиков, возмущенных снижением высокого
              канона.
				  <br>
              &nbsp;&nbsp;&nbsp;Весной 1820 г. он вызван к военному генерал-губернатору
              Петербурга, графу М. А. Милорадовичу для объяснения по поводу распространявшихся
              в списках его политических стихотворений. Пушкину грозит ссылка
              в Сибирь, усилиями старших друзей (Н.М. Карамзина, П.Я.Чаадаева,
              Ф. Н. Глинки) замененная переводом по службе из Петербурга в Екатеринослав.
              Отсюда в конце мая 1820 г. Пушкин едет на Кавказ и в Крым для поправления
              здоровья. Лишь в сентябре он возвращается к месту новой службы —
              в Кишинев, определенный в канцелярию генерала И. Н. Инзова, начальника
              Бессарабского края. Длительные поездки поэта в ноябре 1820 -январе
              1821 г. в Каменку (ставшую одним из декабристских центров) и в Киев,
              путешествие с И. П. Липранди по Молдавии в конце 1821 г., посещение
              Одессы — все это расширило круг впечатлений поэта, наполнило особым
              внутренним смыслом.
				  <br>
              &nbsp;&nbsp;&nbsp;Если поэма "Руслан и Людмила" была итогом
              его школы у лучших русских поэтов, то первая же "южная поэма"
              Пушкина "Кавказский пленник" (1822 г.) поставила его во
              главе всей современной русской литературы, принесла заслуженную
              славу первого поэта, неизменно ему сопутствующую вплоть до конца
              1820-х гг. Позже выходит другая "южная поэма" "Бахчисарайский
              фонтан" (1824 г.). Поэма получилась фрагментарной, словно таящей
              в себе нечто недосказанное, что и придало ей особую прелесть, возбуждающую
              в читательском восприятии сильное эмоциональное поле. Вместе с тем
              поэт пытается обратиться к российской древности, наметив планы поэм
              "Мстислав" и "Вадим" (последний замысел принял
              и драматургическую форму), создает сатирическую поэму "Гавриилиада"
              (1821), поэму "Братья разбойники" (1822; отдельное издание
              в 1827 г.). Со временем в Пушкине созрело убеждение, (поначалу безысходно
              трагическое) что в мире действуют объективные законы, поколебать
              которые человек не в силах, как бы ни были отважны и прекрасны его
              помыслы. В таком ключе был начат в мае 1823 г. в Кишиневе роман
              в стихах "Евгений Онегин"; финал первой главы романа предполагал
              историю путешествия героя за пределами родины по образцу поэмы Байрона
              "Дон Жуан".
				  <br>
              &nbsp;&nbsp;&nbsp;Пока же в июле 1823 г. Пушкин добивается перевода
              по службе в Одессу. Именно в это время Пушкин сознает себя как профессиональный
              литератор, что предопределилось бурным читательским успехом его
              произведений. Сложная обстановка, сложившаяся вокруг Пушкина в Одессе,
              приводит к тому, что поэт подает прошение об отставке, забыв о своем
              положении ссыльного. В результате в июле 1824 г. Пушкин был отстранен
              от службы и направлен в псковское имение Михайловское под надзор
              родителей.
			  </p>
