           <p>
              &nbsp;&nbsp;&nbsp;Несмотря на тяжелые переживания, первая Михайловская
              осень была плодотворной для поэта. Пушкин завершает начатые в Одессе
              стихотворения "Разговор книгопродавца с поэтом", где формулирует
              свое профессиональное кредо, "К морю" — лирическое раздумье
              о судьбе человека эпохи Наполеона и Байрона, о жестокой власти исторических
              обстоятельств над личностью, поэму "Цыганы" (1827 г.),
              продолжает писать роман в стихах. Осенью 1824 г. он возобновляет
              работу над автобиографическими записками, брошенную в самом начале
              в кишиневскую пору, и обдумывает сюжет народной драмы "Борис
              Годунов" (окончена 7 ноября 1825 г. (отдельное издание в 1831
              г.)), пишет шуточную поэму "Граф Нулин".
				  <br>
              &nbsp;&nbsp;&nbsp;В конце 1825–начале 1826 г. Пушкин завершает пятую
              и шестую главы романа "Евгений Онегин", которые в то время
              ему представляются как окончание первой части произведения. В последние
              дни Михайловской ссылки поэт пишет стихотворение "Пророк".
				  <br>
              &nbsp;&nbsp;&nbsp;В ночь с 3 на 4 сентября 1826 г. в Михайловское
              прибывает нарочный от псковского губернатора Б. А. Адеркаса: Пушкин
              в сопровождении фельдъегеря должен явиться в Москву, где ожидал
              коронации новый император, Николай I.
				  <br>
              &nbsp;&nbsp;&nbsp;8 сентября, сразу же после прибытия, Пушкин доставлен
              к царю для личной аудиенции. Поэту по возвращении из ссылки гарантировалось
              личное высочайшее покровительство и освобождение от обычной цензуры.
              Именно в эти годы возникает в творчестве Пушкин интерес к личности
              Петра I, царя-преобразователя. Он становится героем начатого романа
              о прадеде поэта, Абраме Ганнибале, и новой поэмы "Полтава".
				  <br>
              &nbsp;&nbsp;&nbsp;Не заводя собственного дома, Пушкин останавливается
              в Москве и Петербурге ненадолго, мечется между ними, иногда заезжая
              в Михайловское, рвется то на театр военных действий с началом турецкой
              кампании 1828 г., то в китайское посольство; самовольно уезжает
              на Кавказ в 1829 г.
				  <br>
              &nbsp;&nbsp;&nbsp;К этому времени в творчестве поэта обозначился
              новый поворот. Трезвый исторический и социальный анализ действительности
              сочетается с осознанием сложности часто ускользавшего от рационального
              объяснения окружающего мира, что наполняет его творчество ощущением
              тревожного предчувствия, ведет к широкому вторжению фантастики,
              рождает горестные, подчас болезненные воспоминания, напряженный
              интерес к смерти.
			   </p>
