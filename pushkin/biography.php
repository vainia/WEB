            <p>
				  &nbsp;&nbsp;&nbsp;В 1827 г. началось расследование по поводу стихотворения
              "Андрей Шенье" (написанного еще в Михайловском в 1825
              г.), в котором был усмотрен отклик на события 14 декабря 1825 г.,
              а в 1828 г. правительству стала известна кишиневская поэма "Гавриилиада".
              Дела эти были по высочайшему повелению прекращены после объяснений
              Пушкина, но за поэтом был учрежден негласный полицейский надзор.
				  <br>
              &nbsp;&nbsp;&nbsp;Пушкин чувствует необходимость житейских перемен.
              В 1830 г. повторное его сватание к Наталии Николаевне Гончаровой,
              восемнадцатилетней московской красавице, было принято, и осенью
              он отправляется в нижегородское имение своего отца Болдино для вступления
              во владение близлежащей деревней Кистенево, подаренной отцом к свадьбе.
              Холерные карантины задержали поэта на три месяца, и этой поре было
              суждено стать знаменитой Болдинской осенью, наивысшей точкой пушкинского
              творчества, когда из-под его пера вылилась целая библиотека произведений:
              "Повести покойного Ивана Петровича Белкина", "Опыт
              драматических изучений" ("Маленькие трагедии"), последние
              главы "Евгения Онегина", "Домик в Коломне",
              "История села Горюхина", "Сказка о попе и о работнике
              его Балде", несколько набросков критических статей и около
              30 стихотворений.
				  <br>
              &nbsp;&nbsp;&nbsp;Среди болдинских произведений, словно нарочито
              непохожих одно на другое по жанру и по тональности, особенно контрастируют
              друг с другом два цикла: прозаический и драматический. Это два полюса
              его творчества, к которым тяготеют остальные произведения, написанные
              в три осенних месяца 1830 г.
				  <br>
              "Повести Белкина" явились первым из дошедших до нас, завершенным
              прозаическим произведением пушкинской прозы, опыты которой предпринимались
              им неоднократно. Уже в 1821 г. он сформулировал основной закон своего
              прозаического повествования: "Точность и краткость — вот первые
              достоинства прозы. Она требует мыслей и мыслей — без них блестящие
              выражения ни к чему не служат". Эти повести — также своеобразные
              мемуары обыкновенного человека, который, не находя ничего значительного
              в своей жизни, наполняет свои записки пересказом услышанных историй,
              поразивших его воображение своей необычностью.
			   </p>
            <p>
				  &nbsp;&nbsp;&nbsp;18 февраля 1831 г. Пушкин венчается в Москве
              с Н. Н. Гончаровой, весной того же года переселяется с женой в Петербург,
              сняв на лето дачу в Царском Селе. Здесь Пушкин пишет "Письмо
              Онегина", тем самым окончательно завершая работу над романом
              в стихах, который стал его "спутником верным" на протяжении
              восьми лет жизни.
				  <br>
              &nbsp;&nbsp;&nbsp;Новое восприятие действительности, наметившееся
              в творчестве Пушкин в конце 20 гг., требовало углубленных занятий
              историей: в ней следовало найти истоки коренных вопросов современности.
              В 1831 г. он получает разрешение работать в архивах и зачисляется
              вновь на службу в качестве "историографа", получив высочайшее
              задание написать "Историю Петра". Холерные бунты, ужасные
              по своей жестокости, и польские события, поставившие Россию на грань
              войны с Европой, представляются поэту угрозой российской государственности.
              Сильная власть в этих условиях кажется ему залогом спасения России
              — этой идеей вдохновлены его стихотворения "Перед гробницею
              святой...", "Клеветникам России". "Бородинская
              годовщина": последние два вместе со стихотворением В.А. Жуковского
              были напечатаны специальной брошюрой "На взятие Варшавы"
              и вызвали обвинение в политическом ренегатстве, обусловили падение
              популярности Пушкина на Западе и в какой-то мере — в России. Вместе
              с тем Ф. В. Булгарин, связанный с III отделением, обвинял поэта
              в приверженности либеральным идеям.
			   </p>
            <p>
				  &nbsp;&nbsp;&nbsp;С начала 1830-х годов проза в творчестве Пушкина
              начинает превалировать над поэтическими жанрами. "Повести Белкина"
              (изданы в 1831 г.) успеха не имели. Пушкин замышляет широкое эпическое
              полотно, роман из эпохи пугачевщины с героем-дворянином, перешедшим
              на сторону бунтовщиков. Замысел этот на время оставляется из-за
              недостаточных знаний той эпохи, и начинается работа над романом
              "Дубровский" (1832—1833), герой его, мстя за отца, у которого
              несправедливо отняли родовое имение, становится разбойником. Хотя
              сюжетная основа произведения почерпнута Пушкиным из современной
              жизни, в ходе работы роман все больше приобретал черты традиционного
              авантюрного повествования с нетипичной в общем-то для русской действительности
              коллизией. Возможно, предвидя к тому же непреодолимые цензурные
              затруднения с публикацией романа, Пушкин оставляет работу над ним,
              хотя роман был и близок к концу. Замысел произведения о пугачевском
              бунте вновь привлекает его, и верный исторической точности, он прерывает
              на время занятия по изучению Петровской эпохи, штудирует печатные
              источники о Пугачеве, добивается ознакомления с документами о подавлении
              крестьянского восстания (само "Дело Пугачева", строго
              засекреченное, оказывается недоступным), а в 1833 г. предпринимает
              поездку на Волгу и Урал, чтобы воочию увидеть места грозных событий,
              услышать живые предания о пугачевщине. Пушкин едет через Нижний
              Новгород, Казань и Симбирск на Оренбург, а оттуда на Уральск, вдоль
              древнего Яика, переименованного после крестьянского восстания в
              Урал.
				  <br>
              &nbsp;&nbsp;&nbsp;Осенью 1833 г. он возвращается в Болдино. Теперь
              Болдинская осень Пушкина вдвое короче, нежели три года назад, но
              по значению она соразмерна Болдинской осени 1830 г. За полтора месяца
              Пушкин завершает работу над "Историей Пугачева" и "Песнями
              западных славян", начинает работу над повестью "Пиковая
              дама", создает поэмы "Анджело" и "Медный всадник",
              "Сказку о рыбаке и рыбке" и "Сказку о мертвой царевне
              и семи богатырях", стихотворение в октавах "Осень".
				  <br>
              &nbsp;&nbsp;&nbsp;В ноябре 1833 г. Пушкин возвращается в Петербург,
              ощущая необходимость круто переменить жизнь и прежде всего выйти
              из-под опеки двора.
				  <br>
              &nbsp;&nbsp;&nbsp;Накануне 1834 года Николай I производит своего
              историографа в младший придворный чин камер-юнкера. Единственным
              выходом из двусмысленного положения, в котором оказался Пушкин,
              было добиться немедленной отставки. Но семья росла, светская жизнь
              требовала больших расходов, последние же книги Пушкина вышли более
              года назад и большого дохода не принесли, исторические занятия поглощали
              все больше времени, жалование историографа было незначительным,
              и только царь мог разрешить издание новых произведений Пушкина,
              которые могли бы упрочить его материальное положение. Тогда же была
              запрещена поэма "Медный всадник".
				  <br>
              &nbsp;&nbsp;&nbsp;Чтобы как-то выйти из срочных долгов, Пушкин в
              начале 1834 г. быстро дописывает другую, прозаическую петербургскую
              повесть, "Пиковую даму" и помещает ее в журнале "Библиотека
              для чтения", который платил Пушкину незамедлительно и по высшим
              ставкам. Она была начата в Болдине и предназначалась тогда, по-видимому,
              для совместного с В. Ф. Одоевским и Н.В. Гоголем альманаха "Тройчатка".
			   </p>
            <p>
				  &nbsp;&nbsp;&nbsp;В1834 г. Пушкин подает в отставку с просьбой
              сохранить право работы в архивах, необходимое для исполнения "Истории
              Петра". Просьба об отставке была принята, работать в архивах
              ему было запрещено. Пушкин был вынужден прибегнуть к посредничеству
              Жуковского, чтобы урегулировать конфликт. В благодарность за послушание
              ему была выдана испрошенная ранее денежная ссуда в счет пятигодичного
              жалованья. Эта сумма не покрывала и половины долгов Пушкина, с прекращением
              выплаты жалованья приходилось надеяться только на литературные доходы.
              Но профессиональный литератор в России был слишком необычной фигурой.
              Доход его зависел от читательского спроса на произведения. В конце
              1834 — нач. 1835 г. вышло несколько итоговых изданий произведений
              Пушкина: полный текст "Евгения Онегина" (раньше, в 1825—1832
              гг., роман печатался отдельными главами), собрания стихотворений,
              повестей, поэм — все эти книги расходились с трудом. Критика уже
              в полный голос говорила об измельчании таланта Пушкина, о прекращении
              его эпохи в русской литературе. Две осени — 1834 г. (в Болдине)
              и 1835 г. (в Михайловском) были неудачны в творческом отношении.
				  <br>
              &nbsp;&nbsp;&nbsp;Широкой публике, сокрушающейся о падении пушкинского
              таланта, было неведомо, что лучшие его произведения были не пропущены
              в печать, что в те годы шел постоянный, напряженный труд над обширными
              замыслами: "Историей Петра", романом о пугачевщине. В
              творчестве поэта назрели коренные изменения. Пушкин-лирик в эти
              годы становится преимущественно "поэтом для себя". Он
              настойчиво экспериментирует теперь с прозаическими жанрами, которые
              не удовлетворяют его вполне, остаются в замыслах, набросках, черновиках,
              ищет новые формы литературы.
			   </p>
            <p>
				  &nbsp;&nbsp;&nbsp;В этих условиях он находит выход, разом решающий
              многие проблемы. Он основывает журнал, названный "Современником".
              В нем печатались произведения Н.В. Гоголя, А. И. Тургенева, В.А.
              Жуковского, П.А. Вяземского.
				  <br>
              &nbsp;&nbsp;&nbsp;Тем не менее читательского успеха журнал не имел:
              к новому типу серьезного периодического издания, посвященного актуальным
              проблемам, трактуемым но необходимости намеками, русской публике
              предстояло еще привыкнуть. У журнала оказалось всего 600 подписчиков,
              что делало его разорительным для издателя, так как не покрывались
              ни типографские расходы, ни гонорары сотрудников. Два последних
              тома "Современника" Пушкин более чем наполовину наполняет
              своими произведениями, по большей части анонимными.
				  <br>
              &nbsp;&nbsp;&nbsp;В четвертом томе "Современника" был
              наконец напечатан роман "Капитанская дочка".
				  <br>
              &nbsp;&nbsp;&nbsp;Тем же устремлением к будущим поколениям вдохновлено
              и итоговое стихотворение Пушкина, написанное в соответствии с традицией,
              восходящей к Горацию, "Я памятник воздвиг себе нерукотворный..."
              (август 1836 г.).
				  <br>
              &nbsp;&nbsp;&nbsp;Зимой 1837 возник конфликт поэта с Ж. Дантесом,
              принятым на службу в русскую гвардию благодаря покровительству усыновившего
              его голландского посланника барона Л. Геккерена. Ссора привела к
              дуэли. 27 января поэт был смертельно ранен и через два дня скончался.
              Похоронен близ села Михайловское в Псковской губернии в Святогорском
              монастыре (ныне поселок Пушкинские горы)...
			   </p>
