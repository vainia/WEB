						  &nbsp;&nbsp;&nbsp;Дуэль произошла 27 января, в 5-м часу вечера,
		              на Черной речке, при секундантах: секретаре французского посольства
		              д'Аршиаке (со стороны Дантеса) и лицейском товарище Пушкина, Данзасе
		              . Дантес выстрелил первым и смертельно ранил Пушкина в правую сторону
		              живота; Пушкин упал, но потом приподнялся на руку, подозвал Дантеса
		              к барьеру, прицелился, выстрелил и закричал "Браво!",
		              когда увидал, что противник его упал. Но, почувствовав опасность
		              своего положения, Пушкин опять стал добрым и сердечным человеком:
		              прежде всего старался не испугать жены, потом постарался узнать
		              правду от докторов, послал к государю просить прощения для своего
		              секунданта, исповедовался, приобщился, благословил детей, просил
		              не мстить за него, простился с друзьями и книгами, перемогал ужаснейшие
		              физические страдания и утешал, сколько мог, жену. Он скончался в
		              3-м часу пополудни 29 января 1837 года. Его отпевали в придворной
		              конюшенной церкви, после чего А.И. Тургенев отвез его тело для погребения
		              в Святогорский монастырь близ Михайловского.
