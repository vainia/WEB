		            <p style="margin-top: 0px;">
						  &nbsp;&nbsp;&nbsp;В петербургском большом свете, куда Пушкин вступил
		              после женитьбы, он и жена его были "в моде": жена - за
		              красоту и изящество манер, он - за ум и талант. Но их не любили
		              и охотно распространяли о них самые ядовитые сплетни. Даже кроткая
		              Наталья Николаевна возбуждала злую зависть и клевету (см. письмо
		              Пушкина к П.А. Осиповой, № 435); еще сильнее ненавидели самого Пушкина,
		              прошлое которого иные находили сомнительным, a другие - прямо ужасным,
		              и характер которого, и прежде не отличавшийся сдержанностью, теперь,
		              под влиянием тяжелого и часто ложного положения (он должен был представляться
		              богаче, чем был в действительности), бывал резок до крайности. Его
		              агрессивное самолюбие, его злые характеристики, некоторые его стихотворения
		              ("Моя родословная", "На выздоровление Лукулла"
		              и пр.) возбуждали к нему скрытую, но непримиримую злобу очень влиятельных
		              и ловких людей, искусно раздувавших общее к нему недоброжелательство.
		              Пушкин чувствовал его на каждом шагу, раздражался им, и часто сам
		              искал случая сорвать на ком-нибудь свое негодование, чтоб навести
		              страх на остальных.
					   </p>
