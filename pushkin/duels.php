		            <p>
						  &nbsp;&nbsp;&nbsp;4 ноября 1836 года Пушкин получил три экземпляра
		              анонимного послания, заносившего его в орден рогоносцев и, как он
		              был убежден, намекавшего на настойчивые ухаживания за его женой
		              кавалергардского поручика барона Дантеса (Х, 119), красивого и ловкого
		              иностранца, принятого в русскую службу и усыновленного голландским
		              посланником, бароном Геккереном. Пушкин давно уже замечал эти ухаживания
		              (письмо № 478) и воспользовался получением пасквиля, чтобы вмешаться
		              в дело. Он отказал Дантесу от дому, причем Дантес играл роль такую
		              "жалкую", что некоторое сочувствие, которое, может быть,
		              питала Наталья Николаевна к столь "возвышенной страсти"
		              - сочувствие, старательно подогревавшееся бароном Геккереном, -
		              потухло в "заслуженном презрении". Так как сплетни не
		              прекращались, то Пушкин вызвал Дантеса на дуэль; тот принял вызов,
		              но через барона Геккерена (см. письмо № 477; ср. "Воспоминания"
		              графа В.Д. Сологуба, Москва, 1866, стр. 49) просил отсрочки на 15
		              дней. В продолжение этого времени Пушкин узнал, что Дантес сделал
		              предложение его свояченице Ек. Н. Гончаровой - и взял свой вызов
		              назад. Свадьба произошла 10 января 1837 года; друзья Пушкина успокоились,
		              считая дело поконченным. Но излишние и со стороны иных злостные
		              старания сблизить новых родственников снова все испортили: Пушкин
		              очень резко выражал свое презрение Дантесу, который продолжал встречаться
		              с Натальей Николаевной и говорить ей любезности, и Геккерену, который
		              усиленно интриговал против него. Сплетни не прекращались. Выведенный
		              окончательно из терпения, Пушкин послал Геккерену крайне оскорбительное
		              письмо, на которое тот отвечал вызовом от имени Дантеса.
